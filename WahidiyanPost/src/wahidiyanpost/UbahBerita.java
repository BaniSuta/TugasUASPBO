    private void btnubahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnubahActionPerformed
        // TODO add your handling code here:
        String judul, jenis, tgl, isi;
        judul = txtjudul.getText();
        jenis = cbjenis.getSelectedItem().toString();
        isi = txtisi.getText();
        java.util.Date date = datechoose.getDate();
        SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
        tgl = sdf.format(date);
        
        con = koneksi.getConnection();
        try {
            String sql = "UPDATE berita SET judul = ?, jenis = ?, tgl_berita = ?, isi = ? WHERE kode_berita = ?";
            PreparedStatement stmt = con.prepareStatement(sql);
            stmt.setString(1, judul);
            stmt.setString(2, jenis);
            stmt.setString(3, tgl);
            stmt.setString(4, isi);
            stmt.setInt(5, LihatBerita.IDB);

            int rowAffected = stmt.executeUpdate();
            if (rowAffected > 0) {
                cls();
                JOptionPane.showMessageDialog(this, "Berhasil Mengubah Data Berita!", "SQL Information", JOptionPane.INFORMATION_MESSAGE);
                LihatBerita LB = new LihatBerita();
                stmt.close();
                LB.setVisible(true);
                dispose();
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Error: " + e.getMessage(), "SQL Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnubahActionPerformed

