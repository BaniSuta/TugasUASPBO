    private void loadInput() {
        con = koneksi.getConnection();
        try {
            String sql = "SELECT judul,jenis,tgl_berita,isi FROM berita WHERE kode_berita = " + LihatBerita.IDB;
            stat = con.createStatement();
            res = stat.executeQuery(sql);

            if (res.next()) {
                this.txtjudul.setText(res.getString("judul"));
                this.txtisi.setText(res.getString("isi"));
                this.datechoose.setDate(res.getDate("tgl_berita"));
                this.cbjenis.setSelectedItem(res.getString("jenis"));
            }
            res.close();
            stat.close();
            con.close();
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Error: " + e.getMessage(), "SQL Error", JOptionPane.ERROR_MESSAGE);
        }
    }

