    private void tabel() {
        con = koneksi.getConnection();
        DefaultTableModel tb = (DefaultTableModel) table.getModel();
        table.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
        tb.setRowCount(0);

        try {
            stat = con.createStatement();
            String sql = "SELECT kode_berita, username, judul, jenis, tgl_berita, isi FROM berita JOIN akun ON berita.id_penulis = akun.id_akun";
            res = stat.executeQuery(sql);
            while (res.next()) {
                Berita brt = new Berita(
                        res.getInt("kode_berita"),
                        res.getString("username"),
                        res.getString("judul"),
                        res.getString("jenis"),
                        res.getString("tgl_berita"),
                        res.getString("isi"));
                listBerita.add(brt);
            }

            for (Berita brt : listBerita) {
                tb.addRow(new Object[]{
                    brt.getkodeBerita(),
                    brt.getPenulis(),
                    brt.getJudul(),
                    brt.getJenis(),
                    brt.getTanggal(),
                    brt.getIsi()
                });
            }

            res.close();
            stat.close();
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Error: " + e);
        }
    }

    private void tabel(String search) {
        con = koneksi.getConnection();
        DefaultTableModel tb = (DefaultTableModel) table.getModel();
        table.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
        tb.setRowCount(0);

        try {
            stat = con.createStatement();
            String sql = "SELECT kode_berita, username, judul, jenis, tgl_berita, isi FROM berita "
                    + "JOIN akun ON berita.id_penulis = akun.id_akun "
                    + "WHERE username LIKE '%" + search + "%' "
                    + "OR judul LIKE '%" + search + "%' "
                    + "OR jenis LIKE '%" + search + "%' "
                    + "OR isi LIKE '%" + search + "%'";
            res = stat.executeQuery(sql);
            while (res.next()) {
                tb.addRow(new Object[]{
                    res.getInt("kode_berita"),
                    res.getString("username"),
                    res.getString("judul"),
                    res.getString("jenis"),
                    res.getDate("tgl_berita").toString(),
                    res.getString("isi")
                });
            }
            res.close();
            stat.close();
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Error: " + e);
        }
    }

