    private void btnhapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnhapusActionPerformed
        // TODO add your handling code here:
        if (IDB != 0) {
            int response = JOptionPane.showConfirmDialog(this, "Apakah Ingin Menghapus Berita Dengan Kode Berita " + IDB, "Konfirmasi", JOptionPane.YES_NO_OPTION);

            switch (response) {
                case JOptionPane.YES_OPTION:
                    try {
                        Connection conn = koneksi.getConnection();
                        String sql = "DELETE FROM berita WHERE kode_berita = ?";
                        PreparedStatement stmt = conn.prepareStatement(sql);
                        stmt.setInt(1, IDB);

                        int rowAffected = stmt.executeUpdate();
                        if (rowAffected > 0) {
                            for (int i = 0; i < listBerita.size(); i++) {
                                Berita berita = listBerita.get(i);
                                if (berita.getkodeBerita() == IDB) {
                                    listBerita.remove(i);
                                }
                            }
                            IDB = 0;
                            JOptionPane.showMessageDialog(this, "Berhasil Menghapus Berita!", "SQL Information", JOptionPane.INFORMATION_MESSAGE);
                            stmt.close();
                            DefaultTableModel tb = (DefaultTableModel) table.getModel();
                            table.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
                            tb.setRowCount(0);
                            for (Berita brt : listBerita) {
                                tb.addRow(new Object[]{
                                    brt.getkodeBerita(),
                                    brt.getPenulis(),
                                    brt.getJudul(),
                                    brt.getJenis(),
                                    brt.getTanggal(),
                                    brt.getIsi()
                                });
                            }
                        }
                    } catch (SQLException e) {
                        JOptionPane.showMessageDialog(this, "Error: " + e.getMessage(), "SQL Error", JOptionPane.ERROR_MESSAGE);
                    }
                    break;
                default:
                    throw new AssertionError();
            }
        } else {
            JOptionPane.showMessageDialog(this, "Harap Pilih Data Terlebih Dahulu!");
        }
    }//GEN-LAST:event_btnhapusActionPerformed

